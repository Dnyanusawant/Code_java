//write a java program to find a area of cicle  whose radius is 2.5cm.
class AreaOfCircle
{
public static void main(String[] args)
 {
     double radius=2.5;
     final double pi=3.14;
     double area =pi*(radius*radius);
     System.out.println("Area Of Circle is ="+area+"cm^ whose radius is 2.5");
  }
}